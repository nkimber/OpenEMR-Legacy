function technologyNode(technology) {
  const wrapper = node("div", "tech-item");
  const color = text(technology.color);
  if (color) {
    wrapper.style.setProperty("--tech-color", color);
  }

  const logo = node("span", "tech-logo");
  logo.setAttribute("aria-hidden", "true");

  const inlineLogo = technologyLogoTemplate(text(technology.id).toLowerCase());
  if (inlineLogo) {
    logo.insertAdjacentHTML("beforeend", inlineLogo);
    logo.classList.add("has-graphic");
  } else if (text(technology.logoUrl)) {
    const image = document.createElement("img");
    image.src = text(technology.logoUrl);
    image.alt = "";
    image.loading = "lazy";
    image.addEventListener("error", () => logo.classList.remove("has-graphic"));
    image.addEventListener("load", () => logo.classList.add("has-graphic"));
    logo.appendChild(image);
  }

  logo.appendChild(node("span", "tech-logo-fallback", technologyFallbackText(technology)));

  const textGroup = node("span", "tech-text");
  textGroup.appendChild(node("small", null, text(technology.label, "Stack")));
  textGroup.appendChild(node("strong", null, text(technology.name, "Technology")));

  wrapper.append(logo, textGroup);
  return wrapper;
}

function techStackNode(technologies) {
  const items = Array.isArray(technologies) ? technologies.filter((technology) => text(technology?.name)) : [];
  if (!items.length) {
    return null;
  }

  const wrapper = node("section", "tech-stack");
  wrapper.setAttribute("aria-label", "Technology stack");
  wrapper.appendChild(node("div", "tech-stack-title", "Technology stack"));
  const grid = node("div", "tech-stack-grid");
  items.forEach((technology) => grid.appendChild(technologyNode(technology)));
  wrapper.appendChild(grid);
  return wrapper;
}

function entryNode(entry) {
  const wrapper = node("article", "entry");
  const header = node("div", "entry-header");
  const labelGroup = node("div");
  labelGroup.appendChild(node("strong", null, text(entry.label, "Entry point")));
  labelGroup.appendChild(node("small", null, text(entry.role, "Demo role")));

  const status = node("span", entry.url ? "status-pill" : "status-pill unavailable", entry.url ? "Available" : "Waiting for URL");
  header.append(labelGroup, status);
  wrapper.appendChild(header);

  const note = text(entry.note);
  if (note) {
    wrapper.appendChild(node("p", "entry-note", note));
  }

  const credentials = Array.isArray(entry.credentials) ? entry.credentials : [];
  if (credentials.length) {
    const credentialList = node("div", "credential-list");
    credentials.forEach((credential) => credentialList.appendChild(credentialNode(credential)));
    wrapper.appendChild(credentialList);
  }

  const link = node("a", entry.url ? "open-link" : "open-link disabled", entry.url ? `Open ${text(entry.label, "site")}` : "URL unavailable");
  if (entry.url) {
    link.href = entry.url;
    link.target = "_blank";
    link.rel = "noreferrer";
  }
  wrapper.appendChild(link);

  return wrapper;
}

function applicationNode(application) {
  const wrapper = node("article", "app-card");
  const header = node("div", "app-card-header");
  const titleGroup = node("div");
  titleGroup.appendChild(node("span", "version-label", text(application.versionLabel, "Demo")));
  titleGroup.appendChild(node("h2", null, text(application.name, "OpenEMR Demo")));
  header.append(titleGroup, node("span", "status-pill", text(application.statusLabel, "Demo")));
  wrapper.appendChild(header);

  const summary = text(application.summary);
  if (summary) {
    wrapper.appendChild(node("p", "summary", summary));
  }

  const tags = Array.isArray(application.tags) ? application.tags.filter(Boolean) : [];
  if (tags.length) {
    const tagRow = node("div", "tag-row");
    tags.forEach((tag) => tagRow.appendChild(node("span", "tag", tag)));
    wrapper.appendChild(tagRow);
  }

  const techStack = techStackNode(application.techStack);
  if (techStack) {
    wrapper.appendChild(techStack);
  }

  const entryList = node("div", "entry-list");
  const entries = Array.isArray(application.entryPoints) ? application.entryPoints : [];
  entries.forEach((entry) => entryList.appendChild(entryNode(entry)));
  wrapper.appendChild(entryList);

  return wrapper;
}

async function loadPortal() {
  try {
    const response = await fetch("./demo-portal-data.json", { cache: "no-store" });
    if (!response.ok) {
      throw new Error(`Portal data returned HTTP ${response.status}`);
    }
    const data = await response.json();
    titleElement.textContent = text(data.title, "OpenEMR Demo Portal");
    subtitleElement.textContent = text(data.subtitle, "Choose an available demo application.");
    generatedAtElement.textContent = formatGeneratedAt(data.generatedAt);

    const notice = text(data.notice);
    if (notice) {
      noticeElement.textContent = notice;
      noticeElement.hidden = false;
    }

    applicationsElement.replaceChildren();
    const applications = Array.isArray(data.applications) ? data.applications : [];
    if (!applications.length) {
      applicationsElement.appendChild(node("div", "empty", "No demo applications have been published yet."));
      return;
    }

    applications.forEach((application) => applicationsElement.appendChild(applicationNode(application)));
  } catch (error) {
    generatedAtElement.textContent = "Portal data unavailable";
    applicationsElement.replaceChildren(node("div", "empty", error instanceof Error ? error.message : String(error)));
  }
}

void loadPortal();
