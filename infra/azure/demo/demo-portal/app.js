const applicationsElement = document.querySelector("#applications");
const titleElement = document.querySelector("#portal-title");
const subtitleElement = document.querySelector("#portal-subtitle");
const noticeElement = document.querySelector("#notice");
const generatedAtElement = document.querySelector("#generated-at");

function text(value, fallback = "") {
  return typeof value === "string" && value.trim() ? value.trim() : fallback;
}

function formatGeneratedAt(value) {
  if (!value) {
    return "Generated by Workbench";
  }

  const date = new Date(value);
  if (Number.isNaN(date.getTime())) {
    return `Generated ${value}`;
  }

  return `Generated ${new Intl.DateTimeFormat(undefined, {
    month: "short",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit"
  }).format(date)}`;
}

function node(tag, className, content) {
  const element = document.createElement(tag);
  if (className) {
    element.className = className;
  }
  if (content !== undefined && content !== null) {
    element.textContent = String(content);
  }
  return element;
}

function credentialNode(credential) {
  const wrapper = node("div", "credential");
  wrapper.appendChild(node("span", null, text(credential.label, "Credential")));

  const username = node("code", null, `Username: ${text(credential.username, "n/a")}`);
  const password = node("code", null, `Password: ${text(credential.password, "n/a")}`);
  wrapper.append(username, password);
  return wrapper;
}

function technologyFallbackText(technology) {
  const explicitText = text(technology.logoText);
  if (explicitText) {
    return explicitText;
  }

  const name = text(technology.name, "?");
  const words = name.split(/[^a-z0-9]+/i).filter(Boolean);
  if (words.length > 1) {
    return words.map((word) => word.charAt(0)).join("").slice(0, 4).toUpperCase();
  }

  return name.slice(0, 4).toUpperCase();
}

